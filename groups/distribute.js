const random = (items) => {
  return items[Math.floor(Math.random()*items.length)];
};

const groupsToNote = JSON.parse(JSON.stringify(groups));
const distribution = {};

groups.forEach(group => {
  distribution[group.name] = {group: group, groups: []};

  let groupToNote;
  for (let i = 1; i <= 4; i++) {
    do {
      groupToNote = random(groupsToNote);
    }
    while (groupToNote.name === group.name || groupToNote.noted > 4 || distribution[group.name].groups.some(g => g.name === groupToNote.name));
    // use groupToNote.noted >= 4 to force exactly 4 groups by group, but this can infinite loop
  
    groupToNote.noted = !groupToNote.noted ? 1 : groupToNote.noted + 1;
    distribution[group.name].groups.push(groupToNote);
  }
});

